function strongSkillsAdvice(state: CharacterState): string {
  // List the actual proficient skills so the advice is build-specific.
  const cls = getClass(state)
  const skillCount =
    (cls?.grantsSkills?.length ?? 0) +
    state.classSkillChoices.length +
    state.raceSkillChoices.length
  if (skillCount >= 5) {
    return 'У вас очень широкий набор навыков — берите на себя большинство проверок вне боя и ведите разведку.'
  }
  return 'Опирайтесь на свои профильные навыки и предлагайте их там, где у вас экспертиза/владение, а не кидайте всё подряд.'
}

function weaknessAdvice(role: Role, isCaster: boolean): string {
  if (isCaster) {
    return 'Слабость — живучесть и ближний бой: держитесь за спинами, носите запасное оружие и не вступайте в размен ударами.'
  }
  if (role === 'tank' || role === 'melee-dps') {
    return 'Слабость — дальние и контролирующие враги: имейте метательное/дальнобойное оружие и просите кастеров снимать контроль.'
  }
  return 'Слабость закрывайте кооперацией: просите танка прикрыть вас, а кастеров — баффнуть перед сложными проверками.'
}

function asiAdvice(state: CharacterState): string {
  const choice = state.level4Choice
  if (choice.kind === 'asi') {
    return 'Вы выбрали повышение характеристик на 4 уровне — это надёжно: выше попадания, урон и спасброски.'
  }
  if (choice.kind === 'feat') {
    return 'Вы выбрали черту на 4 уровне — она задаёт уникальную грань билда; стройте тактику вокруг неё.'
  }
  return 'На 4 уровне выберите ASI (+2 к основной характеристике) для надёжности или черту под ваш стиль — это окно усиления.'
}

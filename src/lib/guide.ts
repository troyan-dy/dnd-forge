export function buildGuide(state: CharacterState): Guide | null {
  const cls = getClass(state)
  if (!cls) return null
  const role = cls.role
  const sub = cls.subclass

  const archetype = `${cls.name} • ${sub.name} • ${roleLabelRu(role)}`

  // --- В БОЮ ---
  const combat: string[] = [
    `Ваша роль в стычке: ${roleDescription(role)}`,
    ROLE_FIRST_TURN[role],
    ...signatureAbilities(state),
    ROLE_POSITIONING[role],
    ROLE_BONUS_REACTION[role],
    ROLE_AVOID[role],
  ]
  if (cls.isCaster) {
    combat.push(concentrationAdvice())
    combat.push(...spellTagAdvice(state))
  }

  // --- ВНЕ БОЯ ---
  const outOfCombat: string[] = [ROLE_OUT_OF_COMBAT[role]]
  outOfCombat.push(strongSkillsAdvice(state))
  outOfCombat.push(weaknessAdvice(role, cls.isCaster))

  // --- ПРОКАЧКА ---
  const leveling: string[] = [...(CLASS_LEVELING[cls.id] ?? [])]
  leveling.push(asiAdvice(state))

  // --- КОМБО ---
  const combos: string[] = [...(CLASS_COMBOS[cls.id] ?? [])]

  return {
    archetype,
    combat: { title: 'В бою', blocks: combat },
    outOfCombat: { title: 'Вне боя', blocks: outOfCombat },
    leveling: { title: 'Прокачка наперёд', blocks: leveling },
    combos: { title: 'Комбо', blocks: combos },
  }
}


function concentrationAdvice(): string {
  return 'Концентрация: одновременно держите только одно концентрационное заклинание. Под уроном кидайте спасбросок Телосложения (СЛ 10 или половина урона) — не теряйте контроль.'
}

function spellTagAdvice(state: CharacterState): string[] {
  const tags = new Set<SpellTag>()
  ;[...state.knownCantrips, ...state.knownSpells].forEach((id) => {
    SPELL_BY_ID[id]?.spellTags.forEach((t) => tags.add(t))
  })
  const out: string[] = []
  if (tags.has('control')) out.push('У вас есть контроль — открывайте бой им, пока враги сгруппированы (Сон, Волна грома).')
  if (tags.has('damage')) out.push('Урон по площади жгите, когда в зоне 2+ врага; одиночные заговоры — на добивание.')
  if (tags.has('healing')) out.push('Лечение приберегайте на «поднять павшего» — Лечащее слово эффективнее по действиям, чем Лечение ран.')
  if (tags.has('buff')) out.push('Баффы (Благословение, Щит веры) вешайте превентивно в первый ход, до того как полетят кубы.')
  if (tags.has('defense')) out.push('Защитные заклинания (Щит, Доспехи мага) держите про запас на реакцию/начало боя.')
  return out
}

